package top.mingempty.domain.other;

/**
 * 静态常量类
 *
 * @author zzhao
 */
public interface DomainConstant {


    /**
     * 字符串常量：{@code "null"} <br>
     * 注意：{@code "null" != null}
     */
    String NULL = "null";

    /**
     * 字符串常量：空字符串 {@code ""}
     */
    String EMPTY = "";

    /**
     * 字符串常量：空格符 {@code " "}
     */
    String SPACE = " ";
}
